package com.upskilljava_1;

public class AccessModifier {	
	
	public	String name = "Bijoy";				//public - any class can access
		
	private	int age = 25;						//private - only same class access
		
	protected int ssn = 1236548;				//Protected - classes in the same package and subclass
	
	String address = "Heritage Tower";			//default - classes in same package can access
	
	
	public String word="bijoy";
	
	int a = 10;
	
	int b=20;
	
	int number = 25;
}
	


/*Public is any class can access /Private is only same class can access /Protected is same classes in the
package and the sub classes can access /And default is only package can access*/